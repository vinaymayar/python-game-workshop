# The modulo operator, or %, computes the remainder of the integer division of two numbers.
# For example:
#   5 % 2 = 1
#
# The modulo operator is useful for:
#   * Determining whether a number is even or odd
#   * Finding multiples of numbers
#   * Getting the last digit of a number

